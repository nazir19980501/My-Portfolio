 (function () {
    const preloader = document.getElementById('preloader');
    const bar = document.getElementById('preloader-bar');
    const percentEl = document.getElementById('preloader-percent');

    let progress = 0;
    let rafId;

    function step() {
      // speed tapers as it approaches 95%
      const delta = Math.max(0.5, 8 * Math.random());
      progress = Math.min(progress + delta, 95);

      bar.style.width = progress + '%';
      percentEl.textContent = Math.round(progress) + '%';

      if (progress < 95) {
        rafId = requestAnimationFrame(() => setTimeout(step, 120));
      }
    }

    step();

    window.addEventListener('load', () => {
      cancelAnimationFrame(rafId);

      // complete to 100%
      bar.style.width = '100%';
      percentEl.textContent = '100%';

      // give the bar a moment to finish, then fade out
      setTimeout(() => {
        preloader.classList.add('opacity-0', 'pointer-events-none');
        setTimeout(() => preloader.remove(), 450); // matches transition
      }, 250);
    });
  })();






